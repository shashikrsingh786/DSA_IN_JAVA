public class Type_conversion {
    public static void main(String[] args) {
        // implicit type

        //example - 1:
        int a = 4;
        float b = a;
        System.out.println(b);

        //example - 2:
        float c = 67;
        int  d = (int)c;
        System.out.println(d);

        // explicit type

        //example - 1:
        char e = 'a';
        int f = e;
        System.out.println(f);


    }
}
